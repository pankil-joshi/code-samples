    <h3> Refund Policy</h3>
    <p>Last updated on: September 13, 2016</p>
    <p>This policy outlines Tagzie’s general refund policy which all of our Seller’s must abide to.</p>

    <p>When you make a purchase through Tagzie, you enter into a contract of sale with the Seller and not Tagzie. Tagzie will intervene to ensure your statutory rights are never violated.</p>
    <p>Our policy lasts 30 days. If 30 days have gone by since your purchase, unfortunately it is unlikely the Seller will offer you a refund or exchange.</p>
    <p>Customers will have a 14 day cooling off period in which they can decide they wish to cancel their order and get a refund once the item has been returned (providing the returned item is eligible for return as per the terms detailed below).</p>
    <p>It is at the Seller’s discretion as to whether they wish to offer a refund beyond the 14 day cooling off period. They may opt for an exchange instead (providing the returned item is eligible for return as per the terms details below).</p>
    <p>To be eligible for a return, your item must be unused and in the same condition that you received it. It must also be in the original packaging.</p>
    <p>Several types of goods are exempt from being returned. Perishable goods such as food, flowers, newspapers or magazines cannot be returned. Products that are intimate or sanitary goods, hazardous materials, or flammable liquids or gases are also exempt from being returned.</p>


    <p>Additional non-returnable items:</p>

    <ul>
        <li>Gift cards </li>

        <li>Digital Software</li>

        <li>Some health and personal care items</li>
        <li>Made-to-order items</li>

    </ul>
    <p>To complete your return, the Seller may require a receipt or proof of purchase.</p>
    <p>Please do not send your purchase back to the manufacturer. Always raise any issues with the Seller first.</p>
    <p>There are certain situations where only partial refunds are granted:</p>

    <ul>
        <li>Book with obvious signs of use</li>
        <li>CD, DVD, VHS tape, software, video game, cassette tape, or vinyl record that have been opened.</li>
        <li>Any item not in its original condition, is damaged or missing parts for reasons not due to the Seller’s error.</li>
        <li>Any item that is returned after 30 days from delivery</li>
    </ul>
    <h4>Refunds (if applicable)</h4>
    <p>Once your return is received and inspected, the Seller will send you an email to notify you that they have received your returned item. They will also notify you of the approval or rejection of your refund.</p>
    <p>If you are approved your refund will be processed and a credit will automatically be applied to your original method of payment within 5 - 10 business days.</p>


    <h4>Late or missing refunds</h4>
    <p>If 10 business days have passed, please contact the Seller or escalate the issue for Tagzie to investigate.</p>
    <h4>Exchanges</h4>
    <p>Replacement items are usually only offered if they are defective or damaged. If you need to exchange it for the same item, please raise the issue with the Seller.</p>
    <h4>Shipping</h4>
    <p> To return your product, you should either raise an issue with the Seller who will provide instructions on how to commence the return, or follow the instructions from your invoice or dispatch email.</p>
    <p>You will be responsible for covering your own shipping costs when returning your item. Shipping costs are non-refundable. If you receive a refund, the original shipping costs will be deducted from your refund.</p>     

    <p>Depending upon where your location, the time it may take for your exchanged product to reach you may vary.</p>
    <p>Tagzie strongly recommends using a trackable shipping service or purchasing shipping insurance as we can’t guarantee that the Seller will receive your returned item.</p>

